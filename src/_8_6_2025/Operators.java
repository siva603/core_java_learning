package _8_6_2025;

public class Operators {
	
	public static void main(String[] args) {
		
		// unary
		int d = 20;
		System.out.println(-d);
		
		
		// binary 
		
		int a = 10;
		int b = 20;
		
		int res = a + b / 2;
	
		System.out.println(res);
		
		// ternary 
		
		int x = a > b ? a : b ;
		System.out.println(x);
	}
}
