package programming_numbers;

public class _2_print_numbers {

	static void pring_1_to_N(int n ) {
		
		for(int i=1; i<=n; i++) {
			System.out.println(i);
		}
		
	}
	
	static void pring_N_to_1(int n ) {
		
		for(int i=n; i>=1; i--) {
			System.out.println(i);
		}
		
	}
	
	static void pring_N_to_M(int n, int m) {
		
		for(int i=n; i<=m; i++) {
			System.out.println(i);
		}
		
	}
	
	public static void main(String[] args) {
		
	}
	
}
