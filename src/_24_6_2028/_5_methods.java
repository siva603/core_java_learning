package _24_6_2028;

public class _5_methods {
	
	
	static int add(int a,int b) {
		return a+b;
	}
	
	

	public static void main(String[] args) {
		System.out.println("start");
		
		int res = add(25,40);
		System.out.println(res);
		
		int res2 = add(40,20);
		System.out.println(res2);
		
		int res3 = add(23,60);
		System.out.println(res3);
		
		System.out.println("end");
	}
}
