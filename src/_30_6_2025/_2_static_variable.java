package _30_6_2025;

public class _2_static_variable {

	static int v = 10;
	static char c ;
	static byte b ;
	static short s ;
	static long l ;
	static float f;
	static double d;
	static boolean bb;
	static String ss;
	
	
	public static void main(String[] args) {
		
		System.out.println("byte : "+b); // 0
		System.out.println("short : "+s); // 0
		System.out.println("int : "+v); // 10
		System.out.println("long : "+l);
		System.out.println("float : "+f);
		System.out.println("double : "+d);
		System.out.println("boolean : "+bb);
		System.out.println("String  : " + ss);
		System.out.println("char : "+c);
	}
}
