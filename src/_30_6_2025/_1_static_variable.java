package _30_6_2025;

public class _1_static_variable {

	
	static int a = 10;
	static int b = 20;
	
	
	public static void main(String[] args) {
		
		_1_static_variable obj = new _1_static_variable();
		
		// obj <- reference variable
		
		
		System.out.println(b);
		System.out.println(_1_static_variable.a);
		System.out.println(obj.a);
	}
	
	
	
	
}
